// bit 5 Operation mode specific			Change set immediately		reserved					reserved
// bit 4 Operation mode specific			New set-point				reserved					Homing operation start
// bit 3 Enable operation
// bit 2 Quick stop
// bit 1 Enable voltage
// bit 0 Switch on
